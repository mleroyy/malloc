#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*negative_limit;

	negative_limit = "-2147483648";
	if (n == -2147483648)
		ft_putstr_fd(negative_limit, fd);
	else
	{
		if ((n > -2147483648) && (n < 0))
		{
			ft_putchar_fd('-', fd);
			n = -n;
		}
		if (n >= 10)
		{
			ft_putnbr_fd(n / 10, fd);
			ft_putnbr_fd(n % 10, fd);
		}
		else
			ft_putchar_fd(n + 48, fd);
	}
}
