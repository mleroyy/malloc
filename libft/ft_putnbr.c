#include "libft.h"

void	ft_putnbr(int n)
{
	char	*negative_limit;

	negative_limit = "-2147483648";
	if (n == -2147483648)
		ft_putstr(negative_limit);
	else
	{
		if ((n > -2147483648) && (n < 0))
		{
			ft_putchar('-');
			n = -n;
		}
		if (n >= 10)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
		else
			ft_putchar(n + 48);
	}
}
