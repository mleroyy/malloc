#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*d;
	char	*s;

	d = dst;
	s = (void *)src;
	while (n > 0)
	{
		*d = *s;
		n--;
		s++;
		d++;
	}
	return (dst);
}
